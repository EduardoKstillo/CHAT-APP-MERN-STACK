import mongoose from "mongoose";

// Define a new schema
const userSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
  gender: {
    type: String,
    required: true,
    enum: ["male", "female"],
  },
  profilePicture: {
    type: String,
    default: "",
  },
}, { timestamps: true });

// Create the model for users and expose it to our app
const User = mongoose.model("User", userSchema);

export default User;